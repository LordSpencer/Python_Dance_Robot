from Myro import *
from Graphics import *
sim = Simulation("My World", 700, 1000, Color("Green"))
sim.setup()
robot1=makeRobot("SimScribbler", sim)
robot2=makeRobot("SimScribbler", sim)
robot2.forward(1.5,2)
robot1.turnTo(0)
robot1.backward(2,2)
robot1.turnTo(0)
robot2.turnTo(180)
robot2.backward(1,1)

def moveR1(c):
    #A Checked
    c.forward(2,2)
    c.move(-1,1)
    wait(7.5)
    c.move(-1,-1)
    wait(2.5)
    c.forward(1,1)
    c.turnBy(90)
    c.forward(1,1)
    #B
    c.turnBy(45)
    c.forward(2,2)
    c.turnBy(-315)
    c.forward(1,1)
    c.turnBy(60)
    c.forward(2,2)
    c.move(-1,-1)
    wait(3)
    c.backward(2,3)
    #C
    c.turnTo(270)
    c.forward(1,1)
    c.move(1,1)
    c.turnBy(-90)
    c.backward(2,2)
def moveR2(c):
    #A Checked
    c.forward(2,2)
    c.move(-1,1)
    wait(7.5)
    c.move(-1,-1)
    wait(2.5)
    c.forward(1,1)
    c.turnBy(-90)
    c.forward(1,1)
    #B 
    c.turnBy(-45)
    c.forward(2,2)
    c.turnBy(315)
    c.forward(1,1)
    c.turnBy(-60)
    c.forward(2,2)
    c.move(-1,1)
    wait(3)
    c.backward(2,3)
    #C
    c.turnTo(270)
    c.forward(1,1)
    c.turnBy(90)
    c.backward(2,2)
doTogether([moveR1,robot1],[moveR2,robot2])
